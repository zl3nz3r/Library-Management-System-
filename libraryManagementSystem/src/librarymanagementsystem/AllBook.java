    public void allBook(){
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("BookID");
        dtm.addColumn("ISBN");
        dtm.addColumn("Book Title");
        dtm.addColumn("Edition");
        dtm.addColumn("Author");
        dtm.addColumn("Catagory");
        dtm.addColumn("Year");
        int size = 0;
        String query = "Select * From book";
        ConnectDB cn = new ConnectDB();
        cn.connectDB();
        ResultSet rs = cn.fetchData(query);
        try {
            while(rs.next()){
              size++;  
              dtm.addRow(new Object[]{
                size,
                  rs.getString(2),
                  rs.getString(3),
                  rs.getString(4),
                  rs.getString(5),
                  rs.getString(6),
                  rs.getString(7),

            });
              tbl1.setModel(dtm);
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
            
        
    }
    
    
    

